-- =============================================================================

-- Get daily API usage count for a user
CREATE OR REPLACE FUNCTION get_daily_usage_count(p_user_id UUID, p_date DATE DEFAULT CURRENT_DATE)
RETURNS INTEGER AS $$
BEGIN
    RETURN (
        SELECT COUNT(*)
        FROM api_usage
        WHERE user_id = p_user_id
        AND date = p_date
    );
END;
$$ LANGUAGE plpgsql STABLE;

-- Get user's subscription tier limits
CREATE OR REPLACE FUNCTION get_user_tier_limit(p_user_id UUID)
RETURNS INTEGER AS $$
DECLARE
    v_tier VARCHAR(50);
BEGIN
    SELECT subscription_tier INTO v_tier
    FROM users
    WHERE id = p_user_id;
    
    RETURN CASE v_tier
        WHEN 'starter' THEN 100
        WHEN 'professional' THEN 1000
        WHEN 'enterprise' THEN 999999
        ELSE 0
    END;
END;
$$ LANGUAGE plpgsql STABLE;

-- Check if user has exceeded their daily limit
CREATE OR REPLACE FUNCTION check_rate_limit(p_user_id UUID)
RETURNS BOOLEAN AS $$
DECLARE
    v_usage INTEGER;
    v_limit INTEGER;
BEGIN
    v_usage := get_daily_usage_count(p_user_id);
    v_limit := get_user_tier_limit(p_user_id);
    
    RETURN v_usage < v_limit;
END;
$$ LANGUAGE plpgsql STABLE;

